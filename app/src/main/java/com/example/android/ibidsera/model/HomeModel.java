public class HomeModel {
    private int count_daftar;
    private int count_jual;
    private int count_masuk;
    private int count_keluar;

    public int getCount_daftar() {
        return count_daftar;
    }

    public void setCount_daftar(int count_daftar) {
        this.count_daftar = count_daftar;
    }

    public int getCount_jual() {
        return count_jual;
    }

    public void setCount_jual(int count_jual) {
        this.count_jual = count_jual;
    }

    public int getCount_masuk() {
        return count_masuk;
    }

    public void setCount_masuk(int count_masuk) {
        this.count_masuk = count_masuk;
    }

    public int getCount_keluar() {
        return count_keluar;
    }

    public void setCount_keluar(int count_keluar) {
        this.count_keluar = count_keluar;
    }
}
